package com.yjgs.dcl;

import com.yjgs.enumdata.CompanyInfoType;


public class CompanyInfo {

	private int 							infoID 				= 0;			//信息ID
	
	private CompanyInfoType 	infoType  			= null;		//信息类型
	
	private String 						infoContent 		= null;		//信息正文

	public int getInfoID() {
		return infoID;
	}

	public void setInfoID(int infoID) {
		this.infoID = infoID;
	}

	public CompanyInfoType getInfoType() {
		return infoType;
	}

	public void setInfoType(CompanyInfoType infoType) {
		this.infoType = infoType;
	}

	public String getInfoContent() {
		return infoContent;
	}

	public void setInfoContent(String infoContent) {
		this.infoContent = infoContent;
	}
	
	
}
