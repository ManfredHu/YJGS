package com.yjgs.dcl;

import java.util.Date;

public class ProductList {
	
	private int 	productID 			= 0;		//产品ID
	
	private int  ProductTypeID		= 0;		//产品类别ID
	
	private String 	productName 		= null;		//产品名称
	
	private Date		PublishTime		= null;			//产品发布时间
	
	private String 	productBreAddr 	    = null;		//产品缩略图地址

	public int getProductID() {
		return productID;
	}

	public void setProductID(int productID) {
		this.productID = productID;
	}

	public int getProductTypeID() {
		return ProductTypeID;
	}

	public void setProductTypeID(int productTypeID) {
		ProductTypeID = productTypeID;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public Date getPublishTime() {
		return PublishTime;
	}

	public void setPublishTime(Date publishTime) {
		PublishTime = publishTime;
	}

	public String getProductBreAddr() {
		return productBreAddr;
	}

	public void setProductBreAddr(String productBreAddr) {
		this.productBreAddr = productBreAddr;
	}
	
	
	

	
	
	

}
