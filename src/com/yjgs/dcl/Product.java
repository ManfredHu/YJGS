package com.yjgs.dcl;

import java.sql.Date;

public class Product {
	
	public 	int 	prodcuctID		=		0;				//产品ID
	
	public 	int 	productTypeID 	=		0;				//产品类型ID
	
	public 	String 	productName		=		null;			//产品名称

	public 	String 	introduct		=		null;			//产品信息介绍
	
	public 	Date 	publishTime 	= 		null;			//产品发布时间

	public int getProdcuctID() {
		return prodcuctID;
	}

	public void setProdcuctID(int prodcuctID) {
		this.prodcuctID = prodcuctID;
	}

	public int getProductTypeID() {
		return productTypeID;
	}

	public void setProductTypeID(int productTypeID) {
		this.productTypeID = productTypeID;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getIntroduct() {
		return introduct;
	}

	public void setIntroduct(String introduct) {
		this.introduct = introduct;
	}

	public Date getPublishTime() {
		return publishTime;
	}

	public void setPublishTime(Date publishTime) {
		this.publishTime = publishTime;
	}
	
	

}
