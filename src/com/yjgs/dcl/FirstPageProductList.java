package com.yjgs.dcl;

public class FirstPageProductList {

	private int 	listItemID 		= 0;	//列表项ID
	
	private int 	productID 	= 0;	//产品ID
	
	private int 	SortingNum 	= 0;	//排列序号

	public int getListItemID() {
		return listItemID;
	}

	public void setListItemID(int listItemID) {
		this.listItemID = listItemID;
	}

	public int getProductID() {
		return productID;
	}

	public void setProductID(int productID) {
		this.productID = productID;
	}

	public int getSortingNum() {
		return SortingNum;
	}

	public void setSortingNum(int sortingNum) {
		SortingNum = sortingNum;
	}
	
	
}
